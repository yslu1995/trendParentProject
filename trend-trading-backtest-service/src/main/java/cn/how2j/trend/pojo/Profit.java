package cn.how2j.trend.pojo;

/**
 * Profit 利润类
 * 有日期和数值两个属性
 */
public class Profit {

    String date;
    float value;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }

}